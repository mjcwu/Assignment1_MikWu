// the 'for' loop is used for 3 different typs of operation
// - first part, declared variables for iteration
// - second part, we write a condition that 'false' will cause
//   the loop to end
// - third part, we write an expression that will eventually
//   cause loop to exit

for(let i=0; i<10; i+=1){
    console.log(`Mambo No. ${i}`)
}